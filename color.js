let color;

function alltypecolors(pType) {
	if (pType == 'grass') {
		 return '#028900';
		
	} else if (pType == 'fire') {

		return '#ff1903';

	} else if (pType == 'water') {

		return '#303eff';

	} else if (pType == 'bug') {

		return 'orange';

	} else if (pType == 'normal') {

		return '#777777';

	} else if (pType == 'poison') {

		return '#800080';

	} else if (pType == 'electric') {

		return '#fecc00';

	} else if (pType == 'ground') {

		return 'rgb(132, 45, 1)';

	} else if (pType == 'fairy') {

		return 'pink';

	} else if (pType == 'rock') {

		return 'rgb(224, 203, 143)';

	} else if (pType == 'ghost') {

		return 'white';

	} else if (pType == 'psychic') {

		return 'rgb(180, 17, 230)';

	} else if (pType == 'fighting') {

		return 'rgb(212, 141, 0)';

	} else if (pType == 'ice') {

		return '#00fafe';

	} else if (pType == 'dragon') {

		return '#a91515';

	} else if (pType == 'dark') {

		return 'black';

	} else if (pType == 'steel') {

		return '#d2d2d2';

	}
}